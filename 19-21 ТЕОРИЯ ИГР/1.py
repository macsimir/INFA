def WIN1(s):
    # Проверяем, можно ли выиграть за один ход
    return (s - 2) == 0 or (s - 5) == 0 or (s // 3) == 0

def LOOSE1(s):
    # Проверяем, что текущая позиция проигрышная
    # (все возможные ходы ведут в выигрышные для противника позиции)
    return not WIN1(s) and WIN1(s-2) and WIN1(s-5) and WIN1(s//3)

def WIN2(s):
    # Проверяем, можно ли выиграть за два хода
    # (есть ход, который ведет в проигрышную для противника позицию)
    return LOOSE1(s-2) or LOOSE1(s-5) or LOOSE1(s//3)

def LOOSE1_2(s):
    # Проверяем условия задачи:
    # 1) Петя не может выиграть за один ход
    # 2) Но может выиграть на втором ходу независимо от ходов Вани
    return not WIN1(s) and WIN2(s-2) and WIN2(s-5) and WIN2(s//3)

# Ищем два наименьших значения S, удовлетворяющих условиям

for s in range(1, 100):
    if LOOSE1_2(s):
        print(s)